																<div class="entry-content clearfix">
																	<p><span style="font-weight: 400">Use this form if you need advice from us on how to use our resources to do your research.</span></p>
																	<p><span style="font-weight: 400">Before you submit an enquiry, you may find your answer in our </span><a href="http://www.nationalarchives.gov.uk/help-with-your-research/"><span style="font-weight: 400">research guides</span></a><span style="font-weight: 400">, which cover popular topics including family history, First World War; and criminals, courts and prisons. </span></p>
																	<blockquote class="emphasis-block">
																		<h2>We do not hold</h2>
																		<ul>
																			<li style="font-weight: 400"><span style="font-weight: 400">birth, marriage and death certificates – please contact the </span><a href="http://www.gro.gov.uk/gro/content/" target="_blank" rel="noopener noreferrer"><span style="font-weight: 400">General Register Office</span></a></li>
																			<li style="font-weight: 400"><span style="font-weight: 400">Second World War personnel records – please contact the </span><a href="https://www.gov.uk/government/organisations/veterans-uk" target="_blank" rel="noopener noreferrer"><span style="font-weight: 400">Ministry of Defence</span></a></li>
																			<li style="font-weight: 400"><span style="font-weight: 400">wills or divorce records since 1858 – please contact the </span><a href="https://www.gov.uk/search-will-probate" target="_blank" rel="noopener noreferrer"><span style="font-weight: 400">Probate Service</span></a><span style="font-weight: 400"> or the </span><a href="https://courttribunalfinder.service.gov.uk/courts/central-family-court" target="_blank" rel="noopener noreferrer"><span style="font-weight: 400">Central Family Court</span></a><span style="font-weight: 400">, respectively</span></li>
																		</ul>
																	</blockquote>

